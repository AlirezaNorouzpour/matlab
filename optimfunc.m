function ObjF = optimfunc(x)

global basemva accuracy maxiter Initial_Loss Total_initiacost busdata_initial linedata_initial cost_initial;
global cost_initial gendata_initial Initial_VD busdata24 r1 c1 r2 c2 Initial_EENS Initial_VSI
global Initial_plsf Ifault_initial

% ---------- ?????????? ESS (??????? ????????? ???) ----------
dt              = 1.0;     % h  ??? ?????? ????? ???????
SOC0            = 0.50;    % -  SOC ????? ?? ?? ?????? (????????? ??? ????????)
SOC_min         = 0.20;    % -
SOC_max         = 0.90;    % -
eta_ch          = 0.95;    % ??????? ????
eta_dis         = 0.90;    % ??????? ?????
E_cap           = 1.00;    % MWh ????? ????? ?? ESS (???????? ?????? ?? ???)
P_charge_max    = 0.25;    % MW  ??? ???? ???? ?? ESS
P_discharge_max = 0.25;    % MW  ??? ???? ????? ?? ESS
c_cycle         = 5.0;     % $/MWh ??????? ??????? ????
alpha_soc       = 1e4;     % ??????? ??? ???? ??? SOC (??????)
beta_soc        = 1e2;     % ??????? ???????? (L1) ???? ???????

% --------------------------------------------------------------
busdata = busdata24;
linedata = linedata_initial;
cost     = cost_initial;
gendata  = gendata_initial;

% ??????? ????
uP     = zeros(r2,1);       % ???? ?????? ?? ???? ?? ?? ?????? ????
penSOC = zeros(r2,1);       % ????????? SOC
essOpC = zeros(r2,1);       % ????????? ??????????? ESS

% ??? ?? ???????: x ?? ?? P_eff ???? ????? ? ???? ????? ????? ???????
for ii = 1:r2
    y     = round(x(ii + r2));         % ????? ?????? ???????
    Preq  = x(ii);                     % ??????? ????/????? (+ ???? / - ?????)

    if y == 0
        % ESS ?? ??? ??? ??? ????
        P_eff = 0;
        SOC_end = SOC0;
    else
        % ??????????? ????
        P_upper =  +P_charge_max;
        P_lower =  -P_discharge_max;

        % ??????????? SOC (?? ???? ???? ????????????)
        % ??? ???? ????: SOC_end = SOC0 + (P * dt * eta_ch) / E_cap  <= SOC_max
        P_soc_up = ((SOC_max - SOC0) * E_cap) / (dt * eta_ch);
        % ??? ????? ????: SOC_end = SOC0 + (P * dt) / (eta_dis * E_cap) >= SOC_min
        %  --> P >= - ((SOC0 - SOC_min) * eta_dis * E_cap) / dt
        P_soc_lo = - ((SOC0 - SOC_min) * eta_dis * E_cap) / dt;

        % ???? ????? ???? ?????
        P_upper = min(P_upper, P_soc_up);
        P_lower = max(P_lower, P_soc_lo);

        % ???? ?????? ?? ?? ?????? ????
        P_eff = min(max(Preq, P_lower), P_upper);

        % SOC ?????? ???
        if P_eff >= 0
            SOC_end = SOC0 + (P_eff * dt * eta_ch) / E_cap;
        else
            SOC_end = SOC0 + (P_eff * dt) / (eta_dis * E_cap);
        end

        % ????? ??? SOC (?? ??? ????? ??? ??? ??? clamp ??????
        % ??? ??? ????????? ???? ????? ?? clamp ???? ?????? ??? ???? ???)
        viol_up = max(0, SOC_end - SOC_max);
        viol_lo = max(0, SOC_min - SOC_end);
        viol    = viol_up + viol_lo;
        penSOC(ii) = alpha_soc * (viol^2) + beta_soc * viol;

        % ????? ??????? ?????? ?? ????? ??????? ???
        E_throughput = abs(P_eff) * dt;   % MWh
        essOpC(ii)   = c_cycle * E_throughput;
    end

    uP(ii) = P_eff;                                   % ????? ???? ????
    busdata(ii,5) = busdata(ii,5) + uP(ii) * y;       % ????? ?? ??? ?????
end

% --------------------------- Power Flow ---------------------------
[Ybus,nbr,nbus,nl,nr,a,y,ybranch,R,Bc] = ybus(linedata);
[Pg,Pd,Qd,V,kb,Qg,Qsh,Pgg,A,deltad,Pdt,P,Q,S,flagconverg] = ...
    lfnewton(busdata, basemva, accuracy, maxiter, Ybus, nbr, nbus, nl, nr);
[PL,B,B0,B00] = bloss(Pg,Pd,Qd,V,kb,Qg,Qsh,Pgg,A,deltad,Ybus,basemva,nbus);

[EENS,~,~] = EENSIndex(linedata,busdata,basemva,accuracy,maxiter,Ybus,nbr,nbus,nl,nr);
[VSI]       = VSIindex(linedata,busdata,basemva,accuracy,maxiter,Ybus,nbr,nbus,nl,nr);
[plsf]      = PLSFindex(Pd,Ybus,V);
[Ifault_OPT]= faultAnalysis(Ybus,abs(V));

% Robust scalarization
Loss_OPT = sum(abs(PL(:)));
if isnan(Loss_OPT) || Loss_OPT == 0
    Loss_OPT = Initial_Loss;
end

VD_optim   = sqrt(sum((1 - abs(V)).^2));
EENS_OPT   = sum(abs(EENS(:)));
VSI_OPT    = sum(abs(VSI(:)));
plsf_curr  = sum(abs(plsf(:)));
plsf_init  = sum(abs(Initial_plsf(:)));
if plsf_curr <= eps, plsf_curr = eps; end
if plsf_init <= eps, plsf_init = eps; end

% -------------------- Generation Cost --------------------
ctr1 = 0;
cost_OPT = [];
for i5 = 1:r2
    if abs(Pg(i5)) ~= 0
        ctr1 = ctr1 + 1;
        cost_OPT(ctr1) = cost(ctr1,1) + abs(Pg(i5)) * cost(ctr1,2) + (Pg(i5)^2) * cost(ctr1,3);
    end
end

% -------------------- ESS cost components --------------------
ESS_cost_total = sum(essOpC) + sum(penSOC);

Total_cost_OPT = sum(cost_OPT) + ESS_cost_total;

% -------------------- Objective (scalar-safe) ------------------------
if flagconverg == 1
    ObjF = inf;
else
    denom_cost  = max(Total_initiacost, eps);
    denom_loss  = max(Initial_Loss, eps);
    denom_vd    = max(Initial_VD, eps);
    denom_eens  = max(Initial_EENS, eps);
    denom_vsi   = max(Initial_VSI, eps);

    F1 = abs(Total_cost_OPT / denom_cost);
    F2 = abs(Loss_OPT        / denom_loss);
    F3 = abs(VD_optim        / denom_vd);
    F4 = abs(EENS_OPT        / denom_eens);
    F5 = abs(VSI_OPT         / denom_vsi);
    F6 = abs(plsf_init       / plsf_curr);

    denomIf = max(abs(Ifault_initial), 1e-9);
    ratioIf = Ifault_OPT ./ denomIf;
    F7 = abs(sum(1 - abs(ratioIf(:))));

    ObjF = F1 + F2 + F3 + F4 + F5 + F6 + F7;
end
